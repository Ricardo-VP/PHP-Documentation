    var_dump($x != $y); // != valores distintos
    echo "<br>";
    var_dump($x > $y); // > , < , <= y >= para comparar numeros
    echo "<br>";
    var_dump($x && $y); // && y o and
    echo "<br>";
    var_dump($x < $y || $y > 3); // || o, or
