    // -- Operadores de comparacion --
    echo "<h2>Operadores de comparacion</h2>";
    $x = 4;
    $y = 5;
    echo $x == $y; // return true (1)
    var_dump($x == $y); // var_dump muestra true o false
    echo "<br>";
    var_dump($x <> $y); // <> Valores diferentes
